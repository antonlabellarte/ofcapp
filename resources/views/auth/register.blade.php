      function validateForm() {
        var nomeUtente = document.getElementById('name').value;
        var email = document.getElementById('email').value;
        var password = document.getElementById('password').value;

        if (nomeUtente === '' || email === '' || password === '') {
          document.getElementById('emptyFieldAlert').style.display = 'block';
          document.getElementById('emptyFieldAlert').innerHTML = "I campi contrassegnati sono obbligatori";
          
          if (document.getElementById('name').value === ''){
            document.getElementById("name").style.borderColor = "#dc3545";
          } else {
            document.getElementById("name").style.borderColor = "black";
          }
          
          if (document.getElementById('password').value === ''){
            document.getElementById("password").style.borderColor = "#dc3545";
          } else {
            document.getElementById("password").style.borderColor = "black";
          }
          
          if (document.getElementById('email').value === ''){
            document.getElementById("email").style.borderColor = "#dc3545";
          } else {
            document.getElementById("email").style.borderColor = "black";
          }
          return false;
        }

        document.getElementById('emptyFieldAlert').innerHTML = "Utente registrato!";
        return true;
      }
    </script>
